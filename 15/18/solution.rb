
InitialConfiguration = Struct.new(:text)
class InitialConfiguration
  def grid
    @grid ||= Lights.new(text.strip.lines.map(&:strip).map(&:chars))
  end
end

Lights = Struct.new(:grid)
class Lights
  def neighbors point
    x, y = point
    (x-1).upto(x+1).flat_map do |t|
      (y-1).upto(y+1).map do |u|
        [t, u] if t >= 0 && u >= 0 && t < grid.size && u < grid[t].size
      end.filter{|point| point && point != [x, y] }
    end
  end

  def neighbors_configuration point
    neighbors(point).map do |coordinate|
      x, y = coordinate
      grid[x][y]
    end
  end

  def points
    (0...grid.size).flat_map do |x|
      (0...grid[x].size).flat_map do |y|
        grid[x][y]
      end
    end
  end

  def count
    points.count{ |point| point == '#' }
  end

  def next
    Lights.new(step_grid)
  end

  def print
    grid.each do |row|
      puts row.join
    end
  end

  def step_grid
    (0...grid.size).map do |x|
      (0...grid[x].size).map do |y|
        alive = neighbors_configuration([x, y])
          .count{ |point| point == '#' }
        
        if grid[x][y] == '#'
          alive == 2 || alive == 3 ? '#' : '.'
        else
          alive == 3 ? '#' : '.'
        end
      end
    end
  end
end

