
Id = Struct.new(:text)

class Id
  def count(n)
    text.chars.uniq.each { |ch|
      return true if text.count(ch) == n
    }

    false
  end

  def distance(other)
    different_indices(other).count
  end

  def different_indices(other)
    text.chars
      .each_with_index
      .filter { |ch, i|
        ch != other.text[i]
      }
      .lazy
      .map(&:last)
  end
end

class Warehouse
  def initialize(box_ids)
    @box_ids = box_ids
  end

  def self.of(text)
    new(
      text.strip.split("\n").map{|s| Id[s]}
    )
  end

  def checksum
    two_of_any.count * three_of_any.count
  end

  def two_of_any
    box_ids.filter { |id|
      id.count(2)
    }
  end

  def three_of_any
    box_ids.filter { |id|
      id.count(3)
    }
  end

  def box_ids(&block)
    @box_ids.each(&block).lazy
  end

  def shared_between_correct
    found, other = two_correct
    index = found.different_indices(other).first

    p [index, found, other]
    found.text[0...index] + found.text[index+1..]
  end

  def two_correct
    @box_ids.each { |id|
      if found = @box_ids.filter { |other| id.distance(other) == 1 }.first
        return [found, id]
      end
    }

    nil
  end
end

